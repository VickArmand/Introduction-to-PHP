    <h1>REST API</h1>
   <div>
    <p>
API stands for Application Programming Interface <br>
 API is a set of protocols which acts as a medium of communication between programs.In other words its a way 2 programs talk to each other. <br>
 REST means Representational State Transfer <br>
 It is an architectural style which defines a set of protocolos of how an API is written <br>
 It is eficient since it takes less bandwidth <br>
 It follows a set of principles for design and development of an API <br>

<h3>Principles of a REST API </h3> 
<ul>
    <li>Client server</li>
    <li> Stateless- meaning calls can be made independent of one another </li>
    <li> Cacheable - should hold cacheable data</li>
    <li> Uniform interface </li>
    <li>Layered system</li>
    <li> Code on demand</li>   
</ul>
 
    </p>
   <h3>Working of a REST API</h3>
   <ul>
    <li>Uses HTTP Methods</li>
    <li>Uses CRUD(Create Read Update Delete) where by the following methods are used POST for Create GET for Read PUT for Update DELETE for Delete </li>
   </ul>
